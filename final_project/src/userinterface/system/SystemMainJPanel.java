    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String firstName = this.firstNameTextField.getText();
        String lastName = this.lastNameTextField.getText();
        String phone = this.phoneTextField.getText();
        String email = this.emailTextField.getText();
        String address = this.addressTextArea.getText();
        String zipcode = this.zipCodeTextField.getText();
        
        if (!"".equals(firstName) && !"".equals(lastName) && !"".equals(phone) && !"".equals(email) && !"".equals(address) && !"".equals(zipcode)){
            user.setLastName(lastName);
            user.setFirstName(firstName);
            user.setEmail(email);
            user.setAddress(address);
            user.setPhoneNum(phone);
            user.setZip(zipcode);
            loadAccountDetail();
            DB4OUtil.getInstance().storeSystem(system);
        } else {
            JOptionPane.showMessageDialog(null, "Please enter valid input");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void savePwdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePwdButtonActionPerformed
        // TODO add your handling code here:
        String oldpwd = this.oldPasswordTextField.getText();
        String newpwd = this.newPasswordTextField.getText();
        String confirmpwd = this.confirmPasswordTextField.getText();
        if (!"".equals(oldpwd) && !"".equals(newpwd) && !"".equals(confirmpwd) ){
            if (newpwd.equals(confirmpwd)){
                if (oldpwd.equals(user.getPassword())){
                    user.setPassword(newpwd);
                    DB4OUtil.getInstance().storeSystem(system);
                    this.oldPasswordTextField.setText("");
                    this.newPasswordTextField.setText("");
                    this.confirmPasswordTextField.setText("");
                    JOptionPane.showMessageDialog(null, "Update password successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Please enter old password correctly");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please enter same password");
            }
        }else {
             JOptionPane.showMessageDialog(null, "Please enter valid input");
        }
    }//GEN-LAST:event_savePwdButtonActionPerformed


