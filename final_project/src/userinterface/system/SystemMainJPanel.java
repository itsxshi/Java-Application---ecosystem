    private void enterpriseRemoveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterpriseRemoveButtonActionPerformed
        // TODO add your handling code here:
        if (this.enterpriseList.getSelectedValue() != null) {
            for (Network w : system.getNetworkList()) {
                for (Enterprise e : w.getEnterpriseDirectory().getEnterpriseDir()) {
                    if (this.enterpriseList.getSelectedValue().getName().equals(e.getName()) && this.enterpriseList.getSelectedValue().getType().equals(e.getType())) {
                        w.getEnterpriseDirectory().getEnterpriseDir().remove(e);
                        DB4OUtil.getInstance().storeSystem(system);
                        refreshEnterpriseList();
                        return;
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select network");
        }
    }//GEN-LAST:event_enterpriseRemoveButtonActionPerformed

