    public void refreshCustomerTable() {
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0);
        
        for (Customer c : system.getCustomerDir().getCustomerDir()) {
            CustomerUser u = c.getCustomerUser();
            Object[] rowData = new Object[6];
            rowData[0] = u.getUsername();
            rowData[1] = c;
            rowData[2] = c.getCustomerFirstName();
            rowData[3] = c.getCustomerPhoneNum();
            rowData[4] = c.getCustomerEmail();
            rowData[5] = "TODO";

            model.addRow(rowData);
        }
        customerTable.setModel(model);
    }

    public void refreshNetworkList() {
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Network network : system.getNetworkList()) {
            model.addElement(network.toString());
        }
        this.networkList.setModel(model);
    }

    public void refreshEnterpriseCategory() {
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Enterprise.type type : Enterprise.type.values()) {
            model.addElement(type.name());
        }
        this.enterpriseCategoryList.setModel(model);
    }

    public void refreshEnterpriseList() {
        DefaultListModel<Enterprise> model = new DefaultListModel<>();
        for (Network n : system.getNetworkList()) {
            if (this.networkList.getSelectedValue().equals(n.getName())) {
                for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseDir()) {
                    if (this.enterpriseCategoryList.getSelectedValue() != null && this.enterpriseCategoryList.getSelectedValue().equals(e.getType().name())) {
                        model.addElement(e);
                    }
                }
            }
        }
        this.enterpriseList.setModel(model);
    }

