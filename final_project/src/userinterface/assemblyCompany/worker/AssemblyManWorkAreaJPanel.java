    private void detailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.taskTable.getSelectedRow();
        if (selectedRow >= 0) {
            AssemblyRequest ar = (AssemblyRequest) this.taskTable.getValueAt(selectedRow, 0);
            this.receiverNameTextField.setText(ar.getReceiverName());
            this.receiverPhoneTextField.setText(ar.getReceiverPhone());
            this.assemblyAddressTextArea.setText(ar.getReceiverAddress());
            this.assemblyItemTextArea.setText(ar.getOrderedFurnitures().toString());
        } else {
            JOptionPane.showMessageDialog(null, "please select one request");
        }
    }//GEN-LAST:event_detailBtnActionPerformed

    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.taskTable.getSelectedRow();
        if (selectedRow >= 0) {
            AssemblyRequest ar = (AssemblyRequest) this.taskTable.getValueAt(selectedRow, 0);
            if (ar.getwStatus().equals(WorkRequest.status.Processing)) {
                ar.setwStatus(WorkRequest.status.Confirmed);
                loadWorkRequest();

                DB4OUtil.getInstance().storeSystem(system);
            } else {
                JOptionPane.showMessageDialog(null, "invalid action");
            }
        } else {
            JOptionPane.showMessageDialog(null, "please select one request");
        }
    }//GEN-LAST:event_confirmBtnActionPerformed

    private void completeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completeBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.taskTable.getSelectedRow();
        if (selectedRow >= 0) {
            AssemblyRequest ar = (AssemblyRequest) this.taskTable.getValueAt(selectedRow, 0);
            if (ar.getwStatus().equals(WorkRequest.status.Confirmed)) {
                ar.setwStatus(WorkRequest.status.Completed);

                if (n.getWorkqueue().getOrderById(ar.getCustomerOrderId()) instanceof BuyOrderRequest) {
                    BuyOrderRequest bo = (BuyOrderRequest) n.getWorkqueue().getOrderById(ar.getCustomerOrderId());
                    bo.setwStatus(WorkRequest.status.Completed);
                } else if (n.getWorkqueue().getOrderById(ar.getCustomerOrderId()) instanceof SellOrderRequest) {
//                    WarehouseRequest wr = n.getWorkqueue().createWarehouseRequest(ar.getCustomerOrderId(), ar.getReceiverName(), ar.getReceiverAddress(), ar.getReceiverRequirements(), ar.getReceiverPhone(), user, ar.getOrderedFurnitures());
                    DeliveryRequest dr = n.getWorkqueue().createDeliveryRequest(ar.getCustomerOrderId(),ar.getOrderedFurnitures().getFurnitureDir().get(0).getWarehouse().getId(),null, ar.getReceiverName(), ar.getReceiverAddress(), ar.getReceiverRequirements(), ar.getReceiverPhone(), user, ar.getOrderedFurnitures());
                }  else if (n.getWorkqueue().getOrderById(ar.getCustomerOrderId()) instanceof RentOrderRequest){
                    RentOrderRequest rr= (RentOrderRequest)n.getWorkqueue().getOrderById(ar.getCustomerOrderId());
                    if (!rr.isBack()){
                        DeliveryRequest dr = n.getWorkqueue().createDeliveryRequest(ar.getCustomerOrderId(),ar.getOrderedFurnitures().getFurnitureDir().get(0).getWarehouse().getId(),null, ar.getReceiverName(), ar.getReceiverAddress(), ar.getReceiverRequirements(), ar.getReceiverPhone(), user, ar.getOrderedFurnitures());
                    } else {
                        rr.setwStatus(WorkRequest.status.Completed);
                    }
                }

                loadWorkRequest();

                DB4OUtil.getInstance().storeSystem(system);
            } else {
                JOptionPane.showMessageDialog(null, "invalid action");
            }
        } else {
            JOptionPane.showMessageDialog(null, "please select one request");
        }
    }//GEN-LAST:event_completeBtnActionPerformed


