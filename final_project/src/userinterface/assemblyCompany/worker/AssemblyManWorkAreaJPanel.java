    public void getEnterprise() {
        for (Network n : system.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseDir()) {
                for (User u : e.getUserDir().getUserDir()) {
                    if (u.getUsername().equals(user.getUsername())) {
                        this.e = (AssemblyCompany) e;
                        this.n = n;
                        return;
                    }
                }
            }
        }
    }

    public void loadWorkRequest() {
        DefaultTableModel dtm = (DefaultTableModel) taskTable.getModel();

        dtm.setRowCount(0);
        for (WorkRequest ar : n.getWorkqueue().getWorkRequestList()) {
            if (ar instanceof AssemblyRequest) {
                if ((((AssemblyRequest) ar).getAc() == null) || (((AssemblyRequest) ar).getAc().getName().equals(e.getName()))) {
                    Object[] rowData = new Object[5];
                    rowData[0] = ar;
                    rowData[1] = ar.getRequestDate();
                    rowData[2] = ((AssemblyRequest) ar).getReceiverAddress();
                    rowData[3] = ((AssemblyRequest) ar).getOrderedFurnitures();
                    rowData[4] = ar.getwStatus();
                    dtm.addRow(rowData);
                }
            }
        }
        taskTable.setModel(dtm);
    }

