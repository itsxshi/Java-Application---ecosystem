    private void detailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.taskTable.getSelectedRow();
        if (selectedRow >= 0) {
            WarehouseRequest wr = (WarehouseRequest) this.taskTable.getValueAt(selectedRow, 0);
            receiverNameTxt.setText(wr.getReceiverName());
            receiverPhoneTxt.setText(wr.getReceiverName());
            startDateTxt.setText(wr.getRequestDate().toString());
            itemDetailTxt.setText(wr.getOrderedFurnitures().toString());
        } else {
            JOptionPane.showMessageDialog(null, "please select one request");
        }
    }//GEN-LAST:event_detailBtnActionPerformed

    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.taskTable.getSelectedRow();
        if (selectedRow >= 0) {
            WarehouseRequest wr = (WarehouseRequest) this.taskTable.getValueAt(selectedRow, 0);
            if (wr.getwStatus().equals(WarehouseRequest.status.Processing)){
                wr.setwStatus(WorkRequest.status.Confirmed);
                DB4OUtil.getInstance().storeSystem(system);
                loadWorkRequest();
            } else {
                JOptionPane.showMessageDialog(null, "invalid action");
            }
        } else {
            JOptionPane.showMessageDialog(null, "please select one request");
        }
    }//GEN-LAST:event_confirmBtnActionPerformed

    private void pickupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pickupBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.taskTable.getSelectedRow();
        if (selectedRow >= 0) {
            WarehouseRequest wr = (WarehouseRequest) this.taskTable.getValueAt(selectedRow, 0);
            if (wr.getwStatus().equals(WarehouseRequest.status.Confirmed)){
                
                if (n.getWorkqueue().getOrderById((wr.getCustomerOrderId())) instanceof BuyOrderRequest) {
                    wr.setwStatus(WorkRequest.status.Ready);
                    n.getWorkqueue().createDeliveryRequest(wr.getCustomerOrderId(), wr.getOrderId(),null, wr.getReceiverName(),wr.getReceiverAddress(),wr.getReceiverRequirements(),wr.getReceiverPhone(), user, wr.getOrderedFurnitures());
                } else if (n.getWorkqueue().getOrderById((wr.getCustomerOrderId())) instanceof SellOrderRequest) {
                    wr.setwStatus(WorkRequest.status.Completed);
                    n.getFurnitureDir().addFurnitures(wr.getOrderedFurnitures().getFurnitureDir());
                    SellOrderRequest sr = (SellOrderRequest) n.getWorkqueue().getOrderById((wr.getCustomerOrderId()));
                    sr.setwStatus(WorkRequest.status.Completed);
                } else if (n.getWorkqueue().getOrderById((wr.getCustomerOrderId())) instanceof RentOrderRequest) {
                    wr.setwStatus(WorkRequest.status.Stored);
                    n.getFurnitureDir().addFurnitures(wr.getOrderedFurnitures().getFurnitureDir());
                    RentOrderRequest sr = (RentOrderRequest) n.getWorkqueue().getOrderById((wr.getCustomerOrderId()));
                    sr.setwStatus(WorkRequest.status.Stored);
                } 
                
                DB4OUtil.getInstance().storeSystem(system);
                loadWorkRequest();
            } else if  (wr.getwStatus().equals(WarehouseRequest.status.Requested)){
                wr.setwStatus(WorkRequest.status.Ready);
                RentOrderRequest rr = (RentOrderRequest) n.getWorkqueue().getOrderById((wr.getCustomerOrderId()));
                rr.setBack(true);
                n.getWorkqueue().createDeliveryRequest(wr.getCustomerOrderId(), wr.getOrderId(),null, wr.getReceiverName(),wr.getReceiverAddress(),wr.getReceiverRequirements(),wr.getReceiverPhone(), user, wr.getOrderedFurnitures());
                loadWorkRequest();
                DB4OUtil.getInstance().storeSystem(system);
            } else {
                JOptionPane.showMessageDialog(null, "invalid action");
            }
        } else {
            JOptionPane.showMessageDialog(null, "please select one request");
        }
    }//GEN-LAST:event_pickupBtnActionPerformed


