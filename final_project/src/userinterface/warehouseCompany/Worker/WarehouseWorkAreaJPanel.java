    public void getEnterprise() {
        for (Network n : system.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseDir()) {
                for (User u : e.getUserDir().getUserDir()) {
                    if (u.getUsername().equals(user.getUsername())) {
                        this.e = (WarehouseCompany) e;
                        this.n = n;
                        return;
                    }
                }
            }
        }
    }

    public void loadWorkRequest() {
        DefaultTableModel dtm = (DefaultTableModel) taskTable.getModel();

        dtm.setRowCount(0);
        for (WorkRequest wr : n.getWorkqueue().getWorkRequestList()) {
            if (wr instanceof WarehouseRequest) {
                if (((WarehouseRequest) wr).getWarehouse().getName().equals(e.getName())) {
                    Object[] rowData = new Object[4];
                    rowData[0] = wr;
                    rowData[1] = wr.getRequestDate();
                    rowData[2] = ((WarehouseRequest) wr).getReceiverRequirements();
                    rowData[3] = wr.getwStatus();
                    dtm.addRow(rowData);
                }
            }
        }
        taskTable.setModel(dtm);
    }

