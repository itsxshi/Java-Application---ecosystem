    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) orderTable.getModel();
        dtm.setRowCount(0);
        for (Network n : this.system.getNetworkList()) {
            for (WorkRequest r : n.getWorkqueue().getWorkRequestList()) {

                if (r instanceof BuyOrderRequest || r instanceof SellOrderRequest || r instanceof RentOrderRequest) {
                    Object row[] = new Object[4];
                    row[0] = r;
                    if (r instanceof BuyOrderRequest) {
                        row[1] = "Buy Order";
                    } else if (r instanceof SellOrderRequest) {
                        row[1] = "Sell Order";
                    } else if (r instanceof RentOrderRequest) {
                        row[1] = "Rent Order";
                    }
                    row[2] = r.getRequestDate();
                    row[3] = r.getwStatus();
                    dtm.addRow(row);
                }
            }
            // OrderRequest or = (OrderRequest) wr;

        }
        this.orderTable.setModel(dtm);
    }

