    private void orderDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderDetailButtonActionPerformed
        // TODO add your handling code here:
        if (this.orderTable.getSelectedRow() >= 0) {
            WorkRequest wr = (WorkRequest) this.orderTable.getValueAt(this.orderTable.getSelectedRow(), 0);
            OrderDetailPanel panel = new OrderDetailPanel(this.container, wr);
            this.container.add(panel);
            CardLayout layout = (CardLayout) this.container.getLayout();
            layout.next(this.container);
        } else {
            JOptionPane.showMessageDialog(null, "Select your order");
        }

    }//GEN-LAST:event_orderDetailButtonActionPerformed

    private void stopButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stopButtonActionPerformed
        // TODO add your handling code here:
        if (this.orderTable.getSelectedRow() >= 0) {
            WorkRequest wr = (WorkRequest) this.orderTable.getValueAt(this.orderTable.getSelectedRow(), 0);
            if (wr instanceof RentOrderRequest && wr.getwStatus().equals(WorkRequest.status.Stored)) {
                wr.setwStatus(WorkRequest.status.Requested);
                for (Network n : system.getNetworkList()){
                    WorkRequest wrr = n.getWorkqueue().getWarehouseRequestByOrderId(wr.getOrderId());
                    if (wrr!=null){
                        wrr.setwStatus(WorkRequest.status.Requested);
                    }
                }
                populateTable();
            } else {
                JOptionPane.showMessageDialog(null, "Invalid action");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select your order");
        }
    }//GEN-LAST:event_stopButtonActionPerformed


