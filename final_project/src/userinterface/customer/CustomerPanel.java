    private void addCartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCartButtonActionPerformed
        // TODO add your handling code here:
        if (furnitureTable.getSelectedRow() != -1) {
            Furniture f = (Furniture) furnitureTable.getValueAt(furnitureTable.getSelectedRow(), 0);
            int maxQ = f.getQuantity();
            int shoppingCartQ = cart.getFurnitureDir().getFurniture(f) != null ? cart.getFurnitureDir().getFurniture(f).getShoppingQuantity(): 0;
            JFrame frame = new JFrame();
            try {
                int q = Integer.valueOf(JOptionPane.showInputDialog(frame, "Enter your quantity"));
                if (q > maxQ - shoppingCartQ || q == 0) {
                    JOptionPane.showMessageDialog(null, "input valid number");
                } else {
                    Furniture f1 = new Furniture(true, f.getName(), f.getBrand(), f.getCategory(), f.getPrice() , f.getQuantity() , f.getOwner() , f.getWarehouse() );
                    f1.setShoppingQuantity(q);
                    cart.getFurnitureDir().addFurniture(f1);
                    JOptionPane.showMessageDialog(null, "add successfully");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "input valid number");
            }
        } else {
            JOptionPane.showMessageDialog(null, "select one furniture");
        }


    }//GEN-LAST:event_addCartButtonActionPerformed

