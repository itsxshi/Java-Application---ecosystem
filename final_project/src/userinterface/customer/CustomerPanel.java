    public void loadFurnitureTable() {
        Network n = (Network) cityComboBox.getSelectedItem();
        Furniture.category c = (Furniture.category) categoryComboBox.getSelectedItem();

        DefaultTableModel dtm = (DefaultTableModel) furnitureTable.getModel();
        dtm.setRowCount(0);
        for (Furniture f : n.getFurnitureDir().getFurnitureDir()) {
            if (f.getCategory().equals(c)) {
                Object[] rowData = new Object[5];
                rowData[0] = f;
                rowData[1] = f.getBrand();
                rowData[2] = f.getOwner();
                rowData[3] = f.getPrice();
                rowData[4] = f.getQuantity();
                dtm.addRow(rowData);
            }
        }
        furnitureTable.setModel(dtm);
        this.cityComboBox.setEnabled(false);
    }

