    public void loadData() {
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        for (Furniture.category c : Furniture.category.values()) {
            dcbm.addElement(c);
        }

        this.categoryComboBox.setModel(dcbm);

        DefaultListModel<Enterprise> dlm = new DefaultListModel<>();
        for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseDir()) {
            if (e.getType().equals(Enterprise.type.WarehouseCompany)) {
                dlm.addElement(e);
            }
        }
        this.enterpriseList.setModel(dlm);

        DefaultListModel<Enterprise> adlm = new DefaultListModel<>();
        for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseDir()) {
            if (e.getType().equals(Enterprise.type.AssemblyCompany)) {
                adlm.addElement(e);
            }
        }
        this.aEnterpriseList.setModel(adlm);
    }
    
    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) this.table.getModel();
        dtm.setRowCount(0);
        for (Furniture f : sells.getFurnitureDir()){
            Object[] data = new Object[3];
            data[0]= f;
            data[1] = f.getPrice();
            data[2] = f.getQuantity();
            dtm.addRow(data);
        }
        this.table.setModel(dtm);
    }
    
