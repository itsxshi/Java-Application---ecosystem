    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String fName = this.nameTextField.getText();
        String brand = this.brandTextField.getText();
        Furniture.category fc = (Furniture.category) this.categoryComboBox.getSelectedItem();
        try {
            double price = Double.parseDouble(this.priceTextField.getText());
            int quantity = Integer.parseInt(this.quantityTextField.getText());
            if (!"".equals(fName) && !"".equals(brand)) {
                sells.addFurniture(new Furniture(true, fName, brand, fc, price, quantity, user, null));
            } else {
                JOptionPane.showMessageDialog(null, "please enter valid input");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "please enter valid number");
            return;
        }
        populateTable();
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if (this.table.getSelectedRow()>=0){
            sells.removeFurniture((Furniture)this.table.getValueAt(this.table.getSelectedRow(), 0));
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "please select your furniture");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


