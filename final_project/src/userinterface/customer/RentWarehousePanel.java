    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if (this.enterpriseList.getSelectedIndex() >= 0 && this.aEnterpriseList.getSelectedIndex() >= 0 && sells.getFurnitureDir().size()>0) {
            Furniture.category fc = (Furniture.category) this.categoryComboBox.getSelectedItem();
            String fName = this.nameTextField.getText();
            String brand = this.brandTextField.getText();
            double price = Double.parseDouble(this.priceTextField.getText());
            int quantity = Integer.parseInt(this.quantityTextField.getText());
            String cName = this.contactNameTextField.getText();
            String address = this.addressTextField.getText();
            String phone = this.phoneTextField.getText();
            WarehouseCompany wc = (WarehouseCompany) this.enterpriseList.getSelectedValue();
            AssemblyCompany ac = (AssemblyCompany) this.aEnterpriseList.getSelectedValue();

            //            Furniture f = new Furniture(true, fName, brand, fc, price, quantity, user, wc);
            //            FurnitureDir fr = new FurnitureDir();
            //            fr.addFurniture(f);
            for (Furniture f : sells.getFurnitureDir()){
                f.setShoppingQuantity(f.getQuantity());
                f.setWarehouse(wc);
            }

            RentOrderRequest rr = this.network.getWorkqueue().createRentOrderRequest(user, sells);
            AssemblyRequest ar = this.network.getWorkqueue().createAssemblyRequest(rr.getOrderId(), ac, cName, address, "", phone, user, sells);

            OrderConfirmationPanel ocPanel = new OrderConfirmationPanel(panel, new ShoppingCart(sells), user, rr.getOrderId(), address, phone, cName);
            this.panel.add(ocPanel);
            CardLayout layout = (CardLayout) this.panel.getLayout();
            layout.next(this.panel);
        } else {
            JOptionPane.showMessageDialog(null, "please select");
        }

    }//GEN-LAST:event_saveButtonActionPerformed

